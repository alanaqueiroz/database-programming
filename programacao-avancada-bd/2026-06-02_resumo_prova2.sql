CREATE OR REPLACE TRIGGER TRG_VALIDA_PESSOA
BEFORE INSERT OR UPDATE OF SALARIO
ON PESSOA
FOR EACH ROW
BEGIN
    IF :NEW.SALARIO <= 0 OR :NEW.SALARIO > 10000 THEN
        RAISE_APPLICATION_ERROR(-20000, 'SALÁRIO FORA DA FAIXA DE VALOR');
    END IF;
END;
