-- ============================================================
-- FUNCTION: INSERIR FABRICANTE COM VALIDAÇÕES E LOG
-- ============================================================
/*
  FUNC_INSERIRFABRICANTE
  Retornos:
   0  → Sucesso
  -1  → P_ID_FAB nulo
  -2  → P_ID_FAB não inteiro
  -3  → P_ID_FAB fora do intervalo 1–9999
  -4  → ID_FAB já existe na tabela
  -5  → P_NOME nulo ou vazio
  -6  → P_NOME com mais de 120 caracteres
  -7  → P_NOME já existe (comparação sem acento, case-insensitive)
  -8  → P_APELIDO informado com mais de 60 caracteres
*/
CREATE OR REPLACE FUNCTION FUNC_INSERIRFABRICANTE (
    P_ID_FAB  IN FABRICANTE.ID_FAB%TYPE,
    P_NOME    IN FABRICANTE.NOME%TYPE,
    P_APELIDO IN FABRICANTE.APELIDO%TYPE
)
RETURN NUMBER
IS
    V_RETORNO   NUMBER(5);
    V_QTDE      NUMBER;
    V_ERRO      NUMBER;
    V_DESC_ERRO VARCHAR2(1000);
BEGIN
    IF P_ID_FAB IS NULL THEN
        V_RETORNO := -1;
    ELSIF P_ID_FAB <> TRUNC(P_ID_FAB) THEN
        V_RETORNO := -2;
    ELSIF P_ID_FAB NOT BETWEEN 1 AND 9999 THEN
        V_RETORNO := -3;
    ELSE
        SELECT COUNT(*) INTO V_QTDE FROM FABRICANTE WHERE ID_FAB = P_ID_FAB;
        IF V_QTDE > 0 THEN
            V_RETORNO := -4;
        ELSE
            IF P_NOME IS NULL OR LENGTH(TRIM(P_NOME)) = 0 THEN
                V_RETORNO := -5;
            ELSIF LENGTH(TRIM(P_NOME)) > 120 THEN
                V_RETORNO := -6;
            ELSE
                SELECT COUNT(*) INTO V_QTDE FROM FABRICANTE
                WHERE TRANSLATE(UPPER(TRIM(NOME)),    'ÁÉÍÓÚÂÊÎÔÛÃÕÀÈÌÒÙÄËÏÖÜÇ', 'AEIOUAEIOUAOAEIOUAEIOUC')
                    = TRANSLATE(UPPER(TRIM(P_NOME)),  'ÁÉÍÓÚÂÊÎÔÛÃÕÀÈÌÒÙÄËÏÖÜÇ', 'AEIOUAEIOUAOAEIOUAEIOUC');
                IF V_QTDE > 0 THEN
                    V_RETORNO := -7;
                ELSE
                    IF P_APELIDO IS NOT NULL AND LENGTH(TRIM(P_APELIDO)) > 60 THEN
                        V_RETORNO := -8;
                    ELSE
                        INSERT INTO FABRICANTE (ID_FAB, NOME, APELIDO)
                        VALUES (P_ID_FAB, TRIM(P_NOME), TRIM(P_APELIDO));
                        V_RETORNO := 0;
                    END IF;
                END IF;
            END IF;
        END IF;
    END IF;
    COMMIT;
    RETURN V_RETORNO;
EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;
        V_ERRO := SQLCODE;
        V_DESC_ERRO := SQLERRM;
        INSERT INTO LOG_EXECUCAO
            (ID_LOG, ERRO, DESC_ERRO, LINHA_ERRO, NOME_CODIGO, PARAMETROS, DTH_ERRO, USUARIO, RESOLVIDO, DESC_RESOLVIDO)
        VALUES
            (SEQ_LOG_EXECUCAO.NEXTVAL, V_ERRO, V_DESC_ERRO, DBMS_UTILITY.FORMAT_ERROR_BACKTRACE,
            'FUNC_INSERIRFABRICANTE',
            'P_ID_FAB = ' || P_ID_FAB || ' | P_NOME = ' || P_NOME || ' | P_APELIDO = ' || P_APELIDO,
            SYSDATE, USER, 'N', NULL);
        COMMIT;
        RETURN SQLCODE;
END;
