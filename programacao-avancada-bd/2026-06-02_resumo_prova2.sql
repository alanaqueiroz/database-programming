-- ============================================================
-- ATIVIDADE: TRIGGER - VALIDAÇÃO DE MÉDIA E FREQUÊNCIA
-- ============================================================
DROP TABLE MEDIAS_ALUNO;
CREATE TABLE MEDIAS_ALUNO(
    RA NUMBER(7) PRIMARY KEY,
    MEDIA NUMBER(4,2) NOT NULL,
    FREQUENCIA NUMBER(5,2) NOT NULL
);

CREATE OR REPLACE TRIGGER TRG_VALIDA_MEDIA
BEFORE INSERT OR UPDATE OF MEDIA, FREQUENCIA
ON MEDIAS_ALUNO
FOR EACH ROW
BEGIN
    IF :NEW.MEDIA < 0 OR :NEW.MEDIA > 10 THEN
        RAISE_APPLICATION_ERROR(-20000, 'MÉDIA DEVE ESTAR ENTRE 0 E 10');
    END IF;
    IF :NEW.FREQUENCIA < 0 OR :NEW.FREQUENCIA > 100 THEN
        RAISE_APPLICATION_ERROR(-20001, 'FREQUÊNCIA DEVE ESTAR ENTRE 0 E 100');
    END IF;
END;
/

-- ============================================================
-- TABELAS DE APOIO: FUNCIONARIOS, LOG E SEQUENCES
-- ============================================================
DROP TABLE FUNCIONARIOS_AQB;
CREATE TABLE FUNCIONARIOS_AQB AS (SELECT * FROM EMPLOYEES);

DROP TABLE FUNCIONARIO_AQB_LOG;
CREATE TABLE FUNCIONARIO_AQB_LOG(
    ID_LOG NUMBER PRIMARY KEY,
    EMPLOYEE_ID NUMBER(6) NOT NULL,
    TIPO_OPERACAO CHAR(1) NOT NULL,
    SALARY_VELHO NUMBER(8,2) NULL,
    SALARY_NOVO NUMBER(8,2) NULL,
    USUARIO VARCHAR2(30) NOT NULL,
    DTH_REGISTRO DATE NOT NULL,
    SQL_VOLTA VARCHAR(4000) NOT NULL
);

DROP SEQUENCE SEQ_FUNCIONARIO_AQB_LOG;
CREATE SEQUENCE SEQ_FUNCIONARIO_AQB_LOG
NOCACHE
NOCYCLE;

DROP TABLE LOG_EXECUCAO;
CREATE TABLE LOG_EXECUCAO(
    ID_LOG NUMBER PRIMARY KEY,
    ERRO NUMBER NOT NULL,
    DESC_ERRO VARCHAR2(1000) NOT NULL,
    LINHA_ERRO VARCHAR2(1000) NOT NULL,
    NOME_CODIGO VARCHAR2(30) NOT NULL,
    PARAMETROS VARCHAR2(1000) NOT NULL,
    DTH_ERRO DATE NOT NULL,
    USUARIO VARCHAR2(30) NOT NULL,
    RESOLVIDO CHAR(1) NOT NULL,
    DESC_RESOLVIDO VARCHAR2(1000)
);

DROP SEQUENCE SEQ_LOG_EXECUCAO;
CREATE SEQUENCE SEQ_LOG_EXECUCAO
START WITH 1
INCREMENT BY 1
NOCACHE
NOCYCLE;

-- ============================================================
-- TRIGGER: LOG DE INSERT/UPDATE/DELETE COM SQL DE VOLTA
-- ============================================================
DROP TRIGGER TRG_LOG_FUNCIONARIOS_AQB;
CREATE OR REPLACE TRIGGER TRG_LOG_FUNCIONARIOS_AQB
BEFORE INSERT OR UPDATE OR DELETE
ON FUNCIONARIOS_AQB
FOR EACH ROW
BEGIN
    IF INSERTING THEN
        INSERT INTO FUNCIONARIO_AQB_LOG(ID_LOG, EMPLOYEE_ID, TIPO_OPERACAO, SALARY_VELHO, SALARY_NOVO, USUARIO, DTH_REGISTRO, SQL_VOLTA)
        VALUES(SEQ_FUNCIONARIO_AQB_LOG.NEXTVAL, :NEW.EMPLOYEE_ID, 'I', NULL, :NEW.SALARY, USER, SYSDATE,
            'DELETE FROM FUNCIONARIOS_AQB WHERE EMPLOYEE_ID = ' || :NEW.EMPLOYEE_ID || ';');
    ELSIF UPDATING THEN
        INSERT INTO FUNCIONARIO_AQB_LOG(ID_LOG, EMPLOYEE_ID, TIPO_OPERACAO, SALARY_VELHO, SALARY_NOVO, USUARIO, DTH_REGISTRO, SQL_VOLTA)
        VALUES(SEQ_FUNCIONARIO_AQB_LOG.NEXTVAL, :NEW.EMPLOYEE_ID, 'U', :OLD.SALARY, :NEW.SALARY, USER, SYSDATE,
            'UPDATE FUNCIONARIOS_AQB SET SALARY = ' || :OLD.SALARY || ' WHERE EMPLOYEE_ID = ' || :NEW.EMPLOYEE_ID || ';');
    ELSIF DELETING THEN
        INSERT INTO FUNCIONARIO_AQB_LOG(ID_LOG, EMPLOYEE_ID, TIPO_OPERACAO, SALARY_VELHO, SALARY_NOVO, USUARIO, DTH_REGISTRO, SQL_VOLTA)
        VALUES(SEQ_FUNCIONARIO_AQB_LOG.NEXTVAL, :OLD.EMPLOYEE_ID, 'D', :OLD.SALARY, NULL, USER, SYSDATE,
            'INSERT INTO FUNCIONARIOS_AQB (EMPLOYEE_ID, FIRST_NAME, LAST_NAME, EMAIL, PHONE_NUMBER, HIRE_DATE, JOB_ID, SALARY, COMMISSION_PCT, MANAGER_ID, DEPARTMENT_ID) VALUES ('
            || TO_CHAR(:OLD.EMPLOYEE_ID) || ','
            || NVL2(:OLD.FIRST_NAME,    '''' || :OLD.FIRST_NAME    || '''', 'NULL') || ','
            || NVL2(:OLD.LAST_NAME,     '''' || :OLD.LAST_NAME     || '''', 'NULL') || ','
            || NVL2(:OLD.EMAIL,         '''' || :OLD.EMAIL         || '''', 'NULL') || ','
            || NVL2(:OLD.PHONE_NUMBER,  '''' || :OLD.PHONE_NUMBER  || '''', 'NULL') || ','
            || NVL2(:OLD.HIRE_DATE, 'TO_DATE(''' || TO_CHAR(:OLD.HIRE_DATE, 'YYYY-MM-DD HH24:MI:SS') || ''',''YYYY-MM-DD HH24:MI:SS'')', 'NULL') || ','
            || NVL2(:OLD.JOB_ID,        '''' || :OLD.JOB_ID        || '''', 'NULL') || ','
            || NVL(TO_CHAR(:OLD.SALARY),        'NULL') || ','
            || NVL(TO_CHAR(:OLD.COMMISSION_PCT),'NULL') || ','
            || NVL(TO_CHAR(:OLD.MANAGER_ID),    'NULL') || ','
            || NVL(TO_CHAR(:OLD.DEPARTMENT_ID), 'NULL')
            || ');');
    END IF;
END;
